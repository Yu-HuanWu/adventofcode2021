def laternfish(fishes, days)
    days.times do 
        newfish= 0
        fishes.each_with_index do |num, i|
            if num == 0
                fishes[i] = 6
                newfish+=1
            else
                fishes[i]= num-1
            end
        end
        newfish.times do
            fishes << 8
        end
        newfish=0
    end
    fishes.length
end

